package com.hengzhiyi.it.pic.exception;

/**
 * 自定义无事务控制的异常
 * 
 * @author liutianlong
 *
 */
public class NoTxException extends RuntimeException
{

	private static final long serialVersionUID = -1004635437539682501L;

	public NoTxException()
	{
		super();
	}

	public NoTxException(String message)
	{
		super(message);
	}

	public NoTxException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public NoTxException(Throwable cause)
	{
		super(cause);
	}

}
